function removeHtmlTags(html) {
  // Регулярное выражение для поиска всех HTML-тегов.
  // /<[^>]*>/ - находит все, что начинается с < и заканчивается >.
  // g - глобальный флаг, чтобы найти все совпадения, а не только первое.
  const regex = /<[^>]*>/g;

  // Используем .replace() для замены найденных тегов на пустую строку.
  const plainText = html.replace(regex, '');

  // Возвращаем очищенный текст.
  return plainText;
}